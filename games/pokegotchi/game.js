// ==========================================================================
// Game State
// ==========================================================================
let gameState = {
    activePokemon: [], // Array of active pet objects
    totalLevel100s: 0,
    helperActive: false,
    lastHelperTick: 0
};

// Converted categorised arrays for luck rolls
let categorisedPokemon = {
    legendary: [],
    "no-evolve": [],
    evolution3: [],
    evolution2: [],
    basic: []
};

// Constants
const DECAY_INTERVAL = 1000; // Tick needs decay every second
const REQUEST_COOLDOWN = 60000; // 1 minute in ms
const HELPER_COOLDOWN = 30000; // 30 seconds in ms
const XP_PER_REQUEST = 25;

// Active timers
let decayIntervalId = null;
let helperTimerId = null;
let helperSecondsLeft = 30;

// ==========================================================================
// Setup & Initialization
// ==========================================================================
document.addEventListener("DOMContentLoaded", () => {
    buildCategorisedLists();
    loadState();
    
    // If empty game state, start with Pikachu
    if (gameState.activePokemon.length === 0) {
        spawnInitialPikachu();
    }
    
    setupCheatForm();
    renderAll();
    
    // Start main game decay loops
    decayIntervalId = setInterval(tickGame, DECAY_INTERVAL);
    
    // Setup overlays click handlers
    document.getElementById("evolutionCloseBtn").addEventListener("click", () => {
        document.getElementById("evolutionOverlay").classList.remove("active");
    });
    
    document.getElementById("newCloseBtn").addEventListener("click", () => {
        document.getElementById("newPokemonOverlay").classList.remove("active");
    });
});

// Build search lists for rolls
function buildCategorisedLists() {
    for (const [key, p] of Object.entries(POKEMON_EVO_DB)) {
        if (categorisedPokemon[p.rarity]) {
            categorisedPokemon[p.rarity].push(key);
        } else {
            categorisedPokemon.basic.push(key); // fallback
        }
    }
}

// Spawn initial Pikachu
function spawnInitialPikachu() {
    const pikachuData = POKEMON_EVO_DB["pikachu"];
    if (pikachuData) {
        gameState.activePokemon.push({
            species: "pikachu",
            id: pikachuData.id,
            name: pikachuData.name,
            rarity: pikachuData.rarity,
            level: 1,
            xp: 0,
            xpNeeded: 100,
            needs: { food: 100, drink: 100, play: 100 },
            activeRequest: null,
            lastRequestTime: Date.now()
        });
        saveState();
    }
}

// ==========================================================================
// Save & Load state
// ==========================================================================
function saveState() {
    localStorage.setItem("pokegotchi_state_save", JSON.stringify(gameState));
}

function loadState() {
    const saved = localStorage.getItem("pokegotchi_state_save");
    if (saved) {
        try {
            const parsed = JSON.parse(saved);
            gameState.activePokemon = parsed.activePokemon || [];
            gameState.totalLevel100s = parsed.totalLevel100s || 0;
            gameState.helperActive = parsed.helperActive || false;
            gameState.lastHelperTick = parsed.lastHelperTick || 0;
            
            if (gameState.helperActive) {
                document.getElementById("helperWidget").classList.remove("hidden");
                helperSecondsLeft = 30;
            }
        } catch (err) {
            console.error("Failed to parse state", err);
        }
    }
}

// ==========================================================================
// Cheat Form Handling
// ==========================================================================
function setupCheatForm() {
    const input = document.getElementById("cheatInput");
    const btn = document.getElementById("cheatBtn");
    
    btn.addEventListener("click", applyCheat);
    input.addEventListener("keydown", (e) => {
        if (e.key === "Enter") {
            applyCheat();
        }
    });
}

function applyCheat() {
    const input = document.getElementById("cheatInput");
    const feedback = document.getElementById("cheatFeedback");
    const code = input.value.trim().toLowerCase();
    
    input.value = "";
    feedback.textContent = "";
    
    if (code === "helper") {
        if (gameState.helperActive) {
            feedback.textContent = "HELPER ALREADY ACTIVE!";
            feedback.style.color = "var(--color-cyan)";
            return;
        }
        gameState.helperActive = true;
        gameState.lastHelperTick = Date.now();
        helperSecondsLeft = 30;
        document.getElementById("helperWidget").classList.remove("hidden");
        saveState();
        feedback.textContent = "HELPER ACTIVATED!";
        feedback.style.color = "var(--color-green)";
    } else {
        feedback.textContent = "INVALID CHEAT CODE";
        feedback.style.color = "var(--color-red)";
    }
}

// ==========================================================================
// Main Ticking Logic (1s interval)
// ==========================================================================
function tickGame() {
    const now = Date.now();
    let stateChanged = false;

    // Decay and request ticking per Pokemon
    gameState.activePokemon.forEach((p, idx) => {
        // Needs Decay:
        // Food: -1% every 3s
        // Drink: -1% every 2s
        // Play: -1% every 4s
        if (Math.random() < (1 / 3)) {
            p.needs.food = Math.max(0, p.needs.food - 1);
            stateChanged = true;
        }
        if (Math.random() < (1 / 2)) {
            p.needs.drink = Math.max(0, p.needs.drink - 1);
            stateChanged = true;
        }
        if (Math.random() < (1 / 4)) {
            p.needs.play = Math.max(0, p.needs.play - 1);
            stateChanged = true;
        }

        // Active request generator: runs if cooldown elapsed and no active request
        if (!p.activeRequest && (now - p.lastRequestTime >= REQUEST_COOLDOWN)) {
            // Find lowest need, or choose randomly
            const options = [];
            if (p.needs.food < 70) options.push("food");
            if (p.needs.drink < 70) options.push("drink");
            if (p.needs.play < 70) options.push("play");
            
            if (options.length === 0) {
                options.push("food", "drink", "play");
            }
            p.activeRequest = options[Math.floor(Math.random() * options.length)];
            p.lastRequestTime = now;
            stateChanged = true;
        }
    });

    // Helper Ticking (30s Autopilot)
    if (gameState.helperActive) {
        helperSecondsLeft--;
        const fill = document.getElementById("helperProgressBarFill");
        const timer = document.getElementById("helperTimer");
        
        if (fill) {
            const pct = ((30 - helperSecondsLeft) / 30) * 100;
            fill.style.width = `${pct}%`;
        }
        if (timer) {
            timer.textContent = `${helperSecondsLeft}s`;
        }

        if (helperSecondsLeft <= 0) {
            // Auto resolve first pending request
            let resolved = false;
            for (let i = 0; i < gameState.activePokemon.length; i++) {
                const pet = gameState.activePokemon[i];
                if (pet.activeRequest) {
                    const req = pet.activeRequest;
                    solveRequest(i, req, true);
                    resolved = true;
                    break;
                }
            }
            
            // Reset helper timer
            helperSecondsLeft = 30;
            if (fill) fill.style.width = "0%";
            stateChanged = true;
        }
    }

    if (stateChanged) {
        saveState();
        renderAll();
    } else {
        // Just update the status bar values / bars to remain responsive
        updateNeedsBars();
    }
}

// ==========================================================================
// Request Resolver (clicks or helper ticks)
// ==========================================================================
window.solveRequest = function(index, type, isHelper = false) {
    const p = gameState.activePokemon[index];
    if (!p || p.activeRequest !== type) return;

    // Satisfy need
    p.needs[type] = 100;
    p.activeRequest = null;
    p.lastRequestTime = Date.now();

    // Give XP
    p.xp += XP_PER_REQUEST;
    
    // Check level up
    let leveledUp = false;
    while (p.xp >= p.xpNeeded) {
        p.xp -= p.xpNeeded;
        p.level++;
        p.xpNeeded = 100 + (p.level - 1) * 10;
        leveledUp = true;
    }

    if (isHelper) {
        const feedback = document.getElementById("cheatFeedback");
        if (feedback) {
            feedback.textContent = `Helper cared for ${p.name}!`;
            feedback.style.color = "var(--color-purple)";
            setTimeout(() => {
                if (feedback.textContent.startsWith("Helper cared for")) {
                    feedback.textContent = "";
                }
            }, 3000);
        }
    }

    // Check Evolution or New Friend Unlock at Level 100
    if (p.level >= 100) {
        triggerLevel100Events(index);
    }

    saveState();
    renderAll();
};

// Handle evolution or spawning
function triggerLevel100Events(index) {
    const p = gameState.activePokemon[index];
    const originalData = POKEMON_EVO_DB[p.species];
    
    if (originalData && originalData.next && originalData.next.length > 0) {
        // Evolve!
        const nextSpecies = originalData.next[Math.floor(Math.random() * originalData.next.length)];
        const nextData = POKEMON_EVO_DB[nextSpecies];
        
        if (nextData) {
            const beforeName = p.name;
            const beforeImg = `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/official-artwork/${p.id}.png`;
            
            p.species = nextSpecies;
            p.id = nextData.id;
            p.name = nextData.name;
            p.rarity = nextData.rarity;
            p.level = 1;
            p.xp = 0;
            p.xpNeeded = 100;
            p.needs = { food: 100, drink: 100, play: 100 };
            p.activeRequest = null;
            p.lastRequestTime = Date.now();
            
            const afterImg = `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/official-artwork/${p.id}.png`;
            
            showEvolutionScreen(beforeName, beforeImg, p.name, afterImg);
        }
    } else {
        // No evolution: stays at L100, spawn a new companion!
        p.level = 100;
        p.xp = 0;
        p.activeRequest = null;
        gameState.totalLevel100s++;
        
        // Spawn a new Pokemon from the luck chart!
        const rolledSpecies = rollNewPokemon();
        const rolledData = POKEMON_EVO_DB[rolledSpecies];
        
        if (rolledData) {
            gameState.activePokemon.push({
                species: rolledSpecies,
                id: rolledData.id,
                name: rolledData.name,
                rarity: rolledData.rarity,
                level: 1,
                xp: 0,
                xpNeeded: 100,
                needs: { food: 100, drink: 100, play: 100 },
                activeRequest: null,
                lastRequestTime: Date.now()
            });
            
            showNewPokemonScreen(rolledData.name, rolledData.id, rolledData.rarity);
        }
    }
}

