function loadState() {
    const saved = localStorage.getItem("pokemon_collector_state");
    if (saved) {
        try {
            const parsed = JSON.parse(saved);
            gameState = {
                coins: parsed.coins || 0,
                inventory: parsed.inventory || {},
                lockedTiles: parsed.lockedTiles || {},
                tileCooldowns: parsed.tileCooldowns || {},
                tradeOffer: parsed.tradeOffer || null,
                lastCheatUsed: parsed.lastCheatUsed || 0,
                hasJonathanHelper: parsed.hasJonathanHelper || false,
                lastHelperPokeTime: parsed.lastHelperPokeTime || 0
            };

            // Check offline helper progress
            if (gameState.hasJonathanHelper && gameState.lastHelperPokeTime > 0) {
                const now = Date.now();
                const elapsed = now - gameState.lastHelperPokeTime;
                const offlineInterval = 30 * 60 * 1000; // 30 minutes in ms
                const pokemonGained = Math.floor(elapsed / offlineInterval);
                if (pokemonGained > 0) {
                    for (let i = 0; i < pokemonGained; i++) {
                        const randomPokemon = ALL_POKEMON_FLAT[Math.floor(Math.random() * ALL_POKEMON_FLAT.length)];
                        if (!gameState.inventory[randomPokemon.name]) {
                            gameState.inventory[randomPokemon.name] = 0;
                        }
                        gameState.inventory[randomPokemon.name]++;
                    }
                    gameState.lastHelperPokeTime += pokemonGained * offlineInterval;
                    saveState();
                    
                    // Delay alert slightly so page has loaded
                    setTimeout(() => {
                        alert("Helper Jonathan collected " + pokemonGained + " Pokemon while you were offline!");
                    }, 500);
                }
            }
        } catch (e) {
            console.error("Failed to parse saved game state", e);
        }
    }
}

